package Ex1POO;

public class GuardarVariaveis {
	private String nome;
	private String Sobrenome;
	private int idade;
	private char sexo;
	
	
	public String getSobrenome() {
		return Sobrenome;
	}
	public void setSobrenome(String sobrenome) {
		Sobrenome = sobrenome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public char getSexo() {
		return sexo;
	}
	public void setSexo(char sexo) {
		this.sexo = sexo;
	}
	public String getNome() {
		return nome;
	}
	
	
}





